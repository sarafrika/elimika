CREATE TABLE user_notifications (
    id BIGSERIAL PRIMARY KEY,
    uuid UUID NOT NULL DEFAULT gen_random_uuid(),
    recipient_uuid UUID NOT NULL,
    notification_id UUID NOT NULL,
    notification_type VARCHAR(80) NOT NULL,
    category VARCHAR(50) NOT NULL,
    priority VARCHAR(20) NOT NULL,
    presentation VARCHAR(20) NOT NULL DEFAULT 'INBOX',
    status VARCHAR(20) NOT NULL DEFAULT 'UNREAD',
    title VARCHAR(255) NOT NULL,
    body TEXT NOT NULL,
    action_url VARCHAR(1000),
    metadata_json JSONB,
    dedupe_key VARCHAR(255),
    occurred_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
    popup_seen_at TIMESTAMP WITH TIME ZONE,
    read_at TIMESTAMP WITH TIME ZONE,
    archived_at TIMESTAMP WITH TIME ZONE,
    created_date TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_date TIMESTAMP WITH TIME ZONE,
    created_by VARCHAR(255),
    updated_by VARCHAR(255),

    CONSTRAINT uk_user_notifications_uuid UNIQUE (uuid),
    CONSTRAINT uk_user_notifications_dedupe UNIQUE (recipient_uuid, dedupe_key),
    CONSTRAINT chk_user_notifications_type CHECK (notification_type IN (
        'COURSE_ENROLLMENT_WELCOME',
        'COURSE_COMPLETION_CERTIFICATE',
        'LEARNING_MILESTONE_ACHIEVED',
        'ASSIGNMENT_DUE_REMINDER',
