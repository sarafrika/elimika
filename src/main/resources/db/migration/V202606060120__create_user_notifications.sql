        'INSTRUCTOR_CLASS_ENROLLMENT_MILESTONE',
        'INSTRUCTOR_CLASS_ENROLLMENT_NOTICE',
        'UPCOMING_CLASS_REMINDER',
        'ASSIGNMENT_DEADLINE_REMINDER',
        'ASSESSMENT_COMPLETED',
        'LEARNING_CERTIFICATE_ISSUED',
        'PROFILE_DOCUMENT_VERIFIED',
        'PROFILE_COMPLETION_REMINDER'
    )),
    CONSTRAINT chk_user_notifications_category CHECK (category IN ('LEARNING_PROGRESS', 'ASSIGNMENTS_GRADING', 'COURSE_MANAGEMENT', 'SOCIAL_LEARNING', 'SYSTEM_ADMIN')),
    CONSTRAINT chk_user_notifications_priority CHECK (priority IN ('LOW', 'NORMAL', 'HIGH', 'CRITICAL')),
    CONSTRAINT chk_user_notifications_presentation CHECK (presentation IN ('POPUP', 'INBOX')),
    CONSTRAINT chk_user_notifications_status CHECK (status IN ('UNREAD', 'READ', 'ARCHIVED'))
);

CREATE INDEX idx_user_notifications_recipient_created ON user_notifications (recipient_uuid, created_date DESC);
CREATE INDEX idx_user_notifications_recipient_status ON user_notifications (recipient_uuid, status);
CREATE INDEX idx_user_notifications_recipient_popup ON user_notifications (recipient_uuid, presentation, popup_seen_at);
CREATE INDEX idx_user_notifications_type ON user_notifications (notification_type);
CREATE INDEX idx_user_notifications_dedupe ON user_notifications (recipient_uuid, dedupe_key);
